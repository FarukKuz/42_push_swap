#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*dest;
	size_t			i;

	i = 0;
	dest = (unsigned char *)s;
	while (i < n)
	{
		if (dest[i] == (unsigned char)c)
			return ((void *)(dest + i));
		i++;
	}
	return ((void *)0);
}
